    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

    String nomeObjeto = txtNome.getText();
    String descricao = txtDescricao.getText();
    String dataOcorrencia = txtData.getText();
    String local = txtLocal.getText();

    if (cbTipoRegistro.getSelectedItem() == null ||
        cbCategoria.getSelectedItem() == null ||
        cbStatus.getSelectedItem() == null) {

        JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        return;
    }

    String tipoRegistro = cbTipoRegistro.getSelectedItem().toString();
    String categoria = cbCategoria.getSelectedItem().toString();
    String status = cbStatus.getSelectedItem().toString();

    if (nomeObjeto.isEmpty() || descricao.isEmpty()
            || dataOcorrencia.isEmpty() || local.isEmpty()) {

        JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        return;
    }

    Objeto obj = new Objeto();
    obj.setNomeObjeto(nomeObjeto);
    obj.setDescricao(descricao);
    obj.setDataOcorrencia(dataOcorrencia);
    obj.setLocal(local);
    obj.setTipoRegistro(tipoRegistro);
    obj.setCategoria(categoria);
    obj.setStatus(status);
    obj.setMatriculaUsuario(usuarioLogado.getMatricula());

    ObjetoDAO dao = new ObjetoDAO();
        try {
            dao.salvar(obj);
            dao.gerarMatch(obj);
        } catch (Exception ex) {
            Logger.getLogger(CadastroObjeto.class.getName()).log(Level.SEVERE, null, ex);
        }

    JOptionPane.showMessageDialog(this, "Objeto salvo com sucesso!");

    txtNome.setText("");
    txtDescricao.setText("");
    txtData.setText("");
    txtLocal.setText("");
    cbTipoRegistro.setSelectedIndex(-1);
    cbCategoria.setSelectedIndex(-1);
    cbStatus.setSelectedIndex(-1);
    
    
    carregarTabela();
      // TODO add your handling code here:
    }//GEN-LAST:event_btnAdicionarActionPerformed

