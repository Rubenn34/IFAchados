    private void configurarTabela() {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[]{"ID", "Tipo", "Nome", "Categoria", "Status", "Data", "Local", "Matrícula"},
            0
        );
        tblObjetos.setModel(modelo);
    }

    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblObjetos.getModel();
        modelo.setRowCount(0);

        ObjetoDAO dao = new ObjetoDAO();
        List<Objeto> lista = dao.buscarTodos();

        for (Objeto o : lista) {
            modelo.addRow(new Object[]{
                o.getId(),
                o.getTipoRegistro(),
                o.getNomeObjeto(),
                o.getCategoria(),
                o.getStatus(),
                o.getDataOcorrencia(),
                o.getLocal(),
                o.getMatriculaUsuario()
            });
        }
    }
